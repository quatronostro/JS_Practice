 // Objects


 var customer = {
    firstName: 'John',
    lastName: 'Doe'
 }  // This is object in js


 console.log(customer)
 console.log(customer.firstName)


// Dor notation
customer.firstName = "Mike"

// Bracket notation 
customer['lastName'] = "Silver"

console.log(`${customer.firstName} ${customer.lastName}`)


// Arrays

var  car  = ["Volvo", "Toyota", "Tesla"]
car[1] = "BMW"
console.log(car[1])
